public class Celsius0 {
    public static void main(String[] args) {
          
        float fahrenheit = 80;        

        // Convert Fahrenheit to Celsius
        float celsius = (fahrenheit - 32) * 5 / 9;

        // Display the result.........
        System.out.println(fahrenheit + " Fahrenheit is equal to " + celsius + " Celsius.");
    }
}
